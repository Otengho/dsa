
#Dictionaries in Python

Dictionary = {} # Creating an empty Dictionary
print("\nEmpty Dictionary: \n---------------------\n")
print(Dictionary)
  
# Creating a Dictionary with mixed keys
Dictionary2 = dict({1: 'Bob', 2: 'William', 'name': ['Okuro', 'William', 29]})
print("\nDictionary with Mixed Keys:\n-------------------------------\n")
print(Dictionary2)
  
# Creating a Dictionary with items inform of pairs/turples
Dictionary3 = dict([(1, 'Williams'), (2, 'Okuro'), ('age', 29)])
print("\nDictionary with each item as a pair:\n-------------------------------------\n")
print(Dictionary3)